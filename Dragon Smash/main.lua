function love.draw()
   love.graphics.setFont(defaultFont)
   love.graphics.scale(1.375)
   if drawMode == 0 then
      drawMap()
      drawHero()
   elseif drawMode == 1 then
      drawMap()
      drawBattle() 
      drawActions()
   elseif drawMode == 2 then
      drawMap()
      drawMenu()
      drawHero()
   elseif drawMode == 3 then
      drawMap()
      drawBattle()
   end
   drawStatus()
end

function love.keypressed(key)
   if keyMute == false then
      if drawMode == 1 then
         if key == ('a') then
            cursorLeft()
         elseif key == ('d') then
            cursorRight()
         elseif key == ('o') then
            battleEvents()
         end
      elseif drawMode == 2 then
         if key == ('a') then
            cursorLeft()
         elseif key == ('d') then
            cursorRight()
         elseif key == ('o') then
            menuEvents()
         end
      end
   end
end

function love.update(dt)
   if drawMode == 0 then
      if love.keyboard.isDown("s") then
         hero.image = s1
         mapY = mapY + 1
         atTile()
         if onTile ~= 1 and onTile ~= 4 then
            steps = steps + 1
            worldEvents()
         else
            mapY = mapY - 1
            end
      elseif love.keyboard.isDown("w") then
         hero.image = n1
         mapY = mapY - 1
         atTile()
         if onTile ~= 1 and onTile ~= 4 then
            steps = steps + 1
            worldEvents()
         else
            mapY = mapY + 1
         end
      elseif love.keyboard.isDown("a") then
         hero.image = w1
         mapX = mapX - 1
         atTile()
         if onTile ~= 1 and onTile ~= 4 then
            steps = steps + 1
            worldEvents()
         else
            mapX = mapX + 1
         end
      elseif love.keyboard.isDown("d") then
         hero.image = e1
         mapX = mapX + 1
         atTile()
         if onTile ~= 1 and onTile ~= 4 then
            steps = steps + 1
            worldEvents()
         else
            mapX = mapX - 1
         end
      elseif love.keyboard.isDown("k") then
         drawMode = 2
         keyMute = false
      end
   end
   buf = buf + dt
   if buf >= 0.24 then
      if hero.image == n1 then
         hero.image = n2
         buf = 0
         return
      elseif hero.image == n2 then
         hero.image = n1
         buf = 0
         return
      elseif hero.image == s1 then
         hero.image = s2
         buf = 0
         return
      elseif hero.image == s2 then
         hero.image = s1
         buf = 0
         return
      elseif hero.image == w1 then
         hero.image = w2
         buf = 0
         return
      elseif hero.image == w2 then
         hero.image = w1
         buf = 0
         return
      elseif hero.image == e1 then
         hero.image = e2
         buf = 0
         return
      elseif hero.image == e2 then
         hero.image = e1
         buf = 0
         return
      end
   end
   love.timer.sleep(0.12)  
end