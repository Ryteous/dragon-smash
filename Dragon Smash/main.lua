      love.timer.sleep(0.12)
      print("run")
      love.timer.sleep(0.12)
      print("away")
      love.timer.sleep(0.8)
      resetEnemies()
      drawMode = 0
   end
   resetSelector()
end

function worldEvents()
   if steps == encounter then
      if onTile == 2 then
         enemy = love.math.random(2,3)
      elseif onTile == 3 then
         enemy = 4
      else
         enemy = love.math.random(1,2)
      end
      resetSelector()
      print("\nA")
      love.timer.sleep(0.12)
      print(enemies[enemy].name)
      love.timer.sleep(0.12)
      print("draws")
      love.timer.sleep(0.12)
      print("near!")
      love.timer.sleep(0.4)
      print("\nCommand?")
      drawMode = 1
      keyMute = false
   end
end

function menuEvents()
   if selection == 0 then
      castHeal()
   elseif selection == 1 then
      talk()
   end
   resetSelector()
   drawMode = 0
end

